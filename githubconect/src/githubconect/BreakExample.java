package githubconect;

public class BreakExample {

	public static void main(String[] args) {
		 int num = 0;

	      while (num <= 10) {
	         if (num == 5) {
	            break; 
	         }
	         System.out.println("num is: " + num);
	         num++;
	      }
	   }
	




	}


